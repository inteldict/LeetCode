class Solution:
    def threeSum(self, nums: List[int]) -> List[List[int]]:
        counts = Counter(nums)
        result = []
        has_zero = False
        if counts[0] > 0:
            has_zero = True
            # Handle the zero case
            if counts[0] >= 3:
                result.append([0, 0, 0])
            del counts[0]
        nums = sorted(counts.keys())

        for i, num in enumerate(nums):
            if has_zero and num > 0 and counts[-num] > 0:
                result.append([-num, 0, num])
            if num % 2 == 0:
                remaining = -num // 2
                if counts[remaining] >= 2:
                    result.append([num, remaining, remaining])
            # find cases where all 3 numbers are different
            target = -num
            # avoid duplicates by searching of half of target range
            for right in nums[i + 1:bisect_right(nums, target // 2)]:
                left = target - right
                if left in counts and left != right:
                    result.append([left, num, right])

        return result

